/* Programa para somar 3 inteiros*/
#include<stdio.h>
int main(){
    int numero1, numero2, numero3;
    printf("Insira o primeiro número inteiro: ");
    scanf("%d",&numero1);
    printf("Insira o segundo numero inteiro: ");
    scanf("%d",&numero2);
    printf("Insira o terceiro numero inteiro: ");
    scanf("%d",&numero3);
    printf("A soma e: %d", numero1+numero2+numero3);
    return 0;
}
